EHMShards = {}
EHMMaps = {}

function EHMInitSkyshards()
	local map
	local achType
	local component = 0
	for i, value in ipairs( temp ) do
		if type( value ) == "string" then
			map = value
			EHMShards[map] = {}
			component = -1
		else
			if component == -1 then --map id
				if value > 0 then
					EHMMaps[value] = map
				end
				component = -2
			elseif component == -2 then --map achievement id
				achType = value
				component = 0
			elseif component == 0 then --x coord
				EHMShards[map][ #EHMShards[map] + 1 ] = { value / 100.0 }
				component = component + 1
			elseif component == 1 then --y coord
				component = component + 1;
				EHMShards[map][ #EHMShards[map] ][component] = value / 100.0
			elseif component == 2 then
				if achType > 0 then --when map has achievement id
					component = component + 1;
					EHMShards[map][ #EHMShards[map] ][component] = achType
					component = component + 1;
					EHMShards[map][ #EHMShards[map] ][component] = value
					component = 0
				else --cyrodiil
					component = component + 1;
					EHMShards[map][ #EHMShards[map] ][component] = value
				end
			elseif component == 3 or achType <= 0 then --cyrodiil
				EHMShards[map][ #EHMShards[map] ][4] = value
				component = 0
			end
		end
	end
end